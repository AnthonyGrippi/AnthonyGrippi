# Incrementing
counter = 0
print("Incrementing starting point: " + str(counter))
counter = counter + 1
print("Counter = " + str(counter))
counter += 1
print("Counter = " + str(counter))


# Decrementing
counter = 10
print("Counter starting point: " + str(counter))
counter = counter - 1
print("Counter = " + str(counter))
counter -= 1
print("Counter = " + str(counter))
